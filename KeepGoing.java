import java.util.*;
public class KeepGoing {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
        String keepGoing = "y";
        while (keepGoing.equals("y")){
        	System.out.println("Keep going(y/n)");
        	keepGoing = input.next();
        } // end while
	} // end main

} // end class def
